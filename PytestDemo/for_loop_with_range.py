# for loop

for i in range(10):# initial value assign to 0......9
    print(i) #0 1 2 3.......9

print("---"*20)
for i in range(3,8):# initial value assign to 3...7
    print(i) #3...7
print("---"*20)

for i in range(3,18,2):# initial value assign to 3...7
    print(i) #3...7
print("---"*20)

for i in range(10,0,-1):# initial value assign to 10
    print(i) #


## for loop with else block
for i in range(10,0,-1):# initial value assign to 10
    print(i) #
else:
    print("End of the program")
